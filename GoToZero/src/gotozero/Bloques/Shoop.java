package gotozero.Bloques;

import gotozero.Block;


public class Shoop extends Block {

    public Shoop(int x, int y, int z) {
        super();
        this.x = x;
        this.y = y;
        this.z = z;

        this.width = 64;
        this.height = 64;
        this.deep = 128;

        this.Tipo = 12;
    }

}
